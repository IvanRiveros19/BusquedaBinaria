    public void mostrarDatos() {
        String datosIntroducidos = "";
        for (int i = 0; i < index; i++) {
            datosIntroducidos += "[" + arrayNumeros[i] + "] ";
        }
        txtDatos.setText(datosIntroducidos);
    }

    public void metodoBurbuja() {
        int i, j, aux;
        for (i = 0; i < arrayNumeros.length - 1; i++) {
            for (j = 0; j < arrayNumeros.length - i - 1; j++) {
                if (arrayNumeros[j + 1] < arrayNumeros[j]) {
                    aux = arrayNumeros[j + 1];
                    arrayNumeros[j + 1] = arrayNumeros[j];
                    arrayNumeros[j] = aux;
                }
            }
        }
        mostrarDatos();
    }

    public int busquedaBinaria(int inicio, int fin) {
        int mitad = (inicio + fin) / 2;
        int auxTamanio = fin - inicio;

        System.out.println("");
        System.out.println("Buscando: " + numeroBuscar);
        System.out.println("Inicio: " + inicio + ", fin: " + fin);
        System.out.println("Middle: " + mitad);
        System.out.println("Array: " + Arrays.toString(arrayNumeros));
        System.out.println("Numero medio: " + arrayNumeros[mitad]);
        System.out.println("Tamaño: " + auxTamanio);
        System.out.println("");

        if (arrayNumeros[mitad] == numeroBuscar) {
            return mitad;
        } else if (auxTamanio == 1) {
            return -1;
        } else if (numeroBuscar > arrayNumeros[mitad]) {
            return busquedaBinaria(mitad, fin);
        } else {
            return busquedaBinaria(0, mitad);
        }
    }
    
