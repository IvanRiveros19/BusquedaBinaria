    private void btnIntroducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntroducirActionPerformed
        try {
            if (index + 1 > tamanioTotal) {
                JOptionPane.showMessageDialog(null, "No se pueden introducir mas numeros al array");
            } else {
                int nuevoNumero = Integer.parseInt(txtInput.getText());
                arrayNumeros[index] = nuevoNumero;
                index++;
            }
            txtInput.setText("");
            mostrarDatos();
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Solo se permiten numeros");
        }
    }//GEN-LAST:event_btnIntroducirActionPerformed

